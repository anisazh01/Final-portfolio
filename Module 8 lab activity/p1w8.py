# The program takes two inputs from a user and prints whether they are equal or not.

def compare_inputs():
    input1 = input("Enter the first value: ")
    input2 = input("Enter the second value: ")

    if input1 == input2:
        print("The inputs are equal.")
    else:
        print("The inputs are not equal.")

compare_inputs()

