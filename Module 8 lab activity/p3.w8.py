# The program takes a list and prints if the value 5 is in that list.


def check_value(lst):
    if 5 in lst:
        print("The value 5 is in the list.")
    else:
        print("The value 5 is not in the list.")

my_list = [1, 2, 3, 4, 5, 6]
check_value(my_list)

